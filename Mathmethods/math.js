//1.Math.abs()
const absValue = Math.abs(-10);
console.log('Absolute Value=',absValue);

//2.Math.ceil()
const ceilValue = Math.ceil(5.6);
console.log('ceil:',ceilValue);

//3.Math.floor()
const floorValue = Math.floor(-3.6)
console.log('floor:',floorValue);

//4.Math.round()
const roundValue = Math.round(1,9);
console.log('Round:', roundValue);

//5.Math.Max()
const maxNumber = Math.max(1,25,45,78,115);
console.log('Max:',maxNumber);

//6.Math.min()
const minNumber = Math.min(0.5,1.9,-2,-0.5,100);
console.log('min:',minNumber);

//7.Math.pow()
const powerValue = Math.pow(3,4);
console.log('power:',powerValue);

//8.Math.sqrt()
const sqrtValue = Math.sqrt(64);
console.log('sqaure root:',sqrtValue);

//9.Math.trunc()
const truncValue = Math.trunc(0.123);
console.log('Truncate:',truncValue);

//10.Math.random()
const randomNumber = Math.random();
console.log('Random number:',randomNumber);



